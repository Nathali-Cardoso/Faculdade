let nome =  'Nath'

// typeof - permite retornar o tipo de dado de uma variável
console.log(typeof(nome))

// parseInt converte uma string em numero inteiro
//parseFloat converte para número decimal
nome = parseInt(nome)

console.log(typeof(nome))

//Conversão de número em string
let valor = 105

valor = String(valor)
console.log(typeof(valor))

//Converte para número
valor = Number(valor)
console.log(typeof(valor)) 

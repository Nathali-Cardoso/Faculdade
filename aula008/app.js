// Array - []

let minhaDeusa = ['Beyoncé', 'Yoncé', 'Bey', 'QueenB']

//Para adicionar um elemento ao final da lista
minhaDeusa.push('Deusa')

//Para adicionar um elemento ao começo da lista
minhaDeusa.unshift('BB')

//Para remover elementos da lista
//pop - remove do final
minhaDeusa.pop()

//shift - remove do inicio
minhaDeusa.shift()

console.log(minhaDeusa)
// length faz a contagens de elementos da lista
console.log(minhaDeusa.length)

// indexOf localiza a posição dos elementos
console.log(minhaDeusa.indexOf('Beyoncé'))